//função declarativa
function funcao(){
    console.log("esta é uma função declarativa")
}

funcao();


//função de expressão
var funcao = function(){
    console.log("Sou uma função de expressão");
}

funcao();

//arrow function- sempre anonimas
var funcaoArrow = () =>{
    console.log("Sou uma função arrow function");
}

funcaoArrow();